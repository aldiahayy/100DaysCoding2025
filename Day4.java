public class day4 {
    public static void main(String[] args) {
        // 1. \n : New Line
        System.out.print("Hari ke 4 dari 100 hari\n");
        System.out.println();
        
        // 2. \t : Horizontal Tab
        System.out.print("Mantap\tbang\n\n");
        
        // 3. \\ :  Backslash
        System.out.println("11\\September\\2025\n");
        
        // 4. \" : Double Quote
        System.out.print("\"Jangan lupa bersyukur\"\n\n");
        
        // 5. \" : Single Quote
        System.out.printf("%-7s \'Hebattt\'","Kata-kata :");
        System.out.println("\n");
        
        // 6. \b : Backspace
        System.out.println("diantara kau dan aku\b\b\n");
        
        // 7. \r : Cariage return
        System.out.println("Aku kan\rMenghilang\n");
        
        // 8. Gabungannya bang
        System.out.print("Kata-kata\t\'bang\'\n\"Ketakutan berasal dari ketidaktahuann\b\"\n");
        System.out.println("Apakah itu benar? tidak atau \riya\\kah\\");
}
}
